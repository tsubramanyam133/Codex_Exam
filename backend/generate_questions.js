const questions = [];

// Match everything from Q[number]. up to the next Q[number]. or end of string
const qRegex = /Q\d+\.\s+([\s\S]*?)(?=Q\d+\.\s+|$)/g;

let match;
while ((match = qRegex.exec(text)) !== null) {
    let block = match[1].trim();
    
    // We expect the block to look like:
    // [Question text]
    // A. [option] | B. [option] ...
    // Ans: [Letter]
    
    // Sometimes there might be a "Week X: ..." line appended at the end of the previous question's Ans block
    // Let's strip out "Week X: ..." if it exists
    block = block.replace(/Week\s+\d+:\s+.*$/i, '').trim();

    const sections = block.match(/(.+?)\n([A-Z]\.\s+.*)\nAns:\s*([A-D])/is);
    if (!sections) {
        console.log('FAILED TO PARSE: ' + block);
        continue;
    }
    const qText = sections[1].replace(/\n/g, ' ').trim();
    let optionsStr = sections[2].trim();
    
    // remove matching ** if it exists
    optionsStr = optionsStr.replace(/\*\*/g, '');
    
    const optionMatches = optionsStr.split('|').map(o => o.trim());
    const options = optionMatches.map(o => o.replace(/^[A-D]\.\s*/, '').trim());
    
    const ansLetter = sections[3].trim().toUpperCase();
    const ansIdx = ansLetter.charCodeAt(0) - 65;

    questions.push({
        questionText: qText,
        options: options,
        correctOptionIndex: ansIdx
    });
}


fs.writeFileSync('C:/Users/tsubr/Codex/backend/questions.json', JSON.stringify(questions, null, 2), 'utf8');

console.log('Successfully wrote questions.json with ' + questions.length + ' questions.');
